class SecondClass {
    // Entry Gate
    // Main is a function,function contains logic(statement)
    // void - keyword(Reserve Word)-no return
    // static- keyword - when class is loaded in memory,then static will load along.
    // static(Eager Loading)
    // public - access outside the package(Folder)
    // String Args[] - Command Line Arguments
    // string - collection of chars,String is a Predefine Class.
    // args- arguments
    // []-box or array(Contain lot of things)
    public static void main(String[] args) {// main start
        System.out.println("Hello World.....");
        // print is a pre define function, used to print on console.
        // print function is access using out object.
        // out object belongs to PrintStream class.
        // out object is created by System Class.

    }// main end
}// class end